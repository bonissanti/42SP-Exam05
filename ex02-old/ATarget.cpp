#include "ATarget.hpp"
#include "ASpell.hpp"

ATarget::ATarget(){

}

ATarget::~ATarget(){

}

ATarget::ATarget(const std::string& type) : type(type){

}

ATarget::ATarget(const ATarget& toCopy) : type(toCopy.type){
	*this = toCopy;
}

ATarget& ATarget::operator=(const ATarget& toCopy){
	if (this != &toCopy){
		this->type = toCopy.type;
	}
	return (*this);
}

std::string ATarget::getType(void) const{
	return (this->type);
}

void ATarget::getHitBySpell(const ASpell& spell) const{
	std::cout << this->type << " has been " << spell.getEffect() << "!" << std::endl;
}