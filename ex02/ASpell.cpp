#include "ASpell.hpp"

ASpell::ASpell(){

}

ASpell::~ASpell(){}

ASpell::ASpell(const std::string name, const std::string effect):name(name), effect(effect){

}

ASpell::ASpell(const ASpell& toCopy) : name(toCopy.name), effect(toCopy.effect){
	*this = toCopy;
}

ASpell& ASpell::operator=(const ASpell& toCopy){
	if (this != &toCopy){
		this->name = toCopy.name;
		this->effect = toCopy.effect;
	}
	return (*this);
}

std::string ASpell::getName(void) const{
	return (this->name);
}

std::string ASpell::getEffect(void) const{
	return (this->effect);
}

void	ASpell::launch(const ATarget& target) const{
	target.getHitBySpell(*this);
}