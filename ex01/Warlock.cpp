#include "Warlock.hpp"

Warlock::Warlock(){

}

Warlock::Warlock(const std::string& name, const std::string& title) : name(name), title(title){
	std::cout << this->name << ": This looks like another boring day." << std::endl;
}

Warlock::~Warlock(){
	std::cout << this->name << ": My job here is done!" << std::endl;
}
Warlock::Warlock(const Warlock& toCopy) : name(toCopy.name), title(toCopy.title){
	*this = toCopy;
}

Warlock& Warlock::operator=(const Warlock& toCopy){
	if (this != &toCopy){
		this->name = toCopy.name;
		this->title = toCopy.title;
	}
	return *this;
}

std::string Warlock::getName(void) const{
	return (this->name);
}

std::string Warlock::getTitle(void) const{
	return (this->title);
}

void	Warlock::setTitle(const std::string& title){
	this->title = title;
}

void	Warlock::introduce(void) const{
	std::cout << this->name << ": I am " << this->name << ", " << this->title << "!" << std::endl;
}

void	Warlock::learnSpell(ASpell *spell){
	this->spellpair[spell->getName()] = spell;
}

void	Warlock::forgetSpell(const std::string& spellName){
	std::map<std::string, ASpell*>::iterator it;
	it = this->spellpair.find(spellName);
	if (it != this->spellpair.end()){
		delete it->second;
		this->spellpair.erase(it);
	}
}

void	Warlock::launchSpell(const std::string& spellName, const ATarget& target){
	std::map<std::string, ASpell*>::iterator it;
	it = this->spellpair.find(spellName);

	if (it != this->spellpair.end()){
		it->second->launch(target);
	}
}